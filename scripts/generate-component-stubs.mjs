/**
 * Generates one JSX stub per catalog component under src/pages/components/<categoryId>/
 * (except button + cards). Writes allStubComponents.js at the components root.
 * Run: node scripts/generate-component-stubs.mjs
 */
import { mkdirSync, writeFileSync } from 'fs'
import { fileURLToPath } from 'url'
import { dirname, join } from 'path'
import { getAllComponentPageMeta, slugToComponentModuleName } from '../src/data/componentPageMeta.js'

const __dirname = dirname(fileURLToPath(import.meta.url))
const componentsDir = join(__dirname, '../src/pages/components')

const SKIP = new Set(['button', 'cards'])

const stubTemplate = `import { stubComponentPage } from '../stubComponentPage'

export default stubComponentPage('SLUG')
`

const meta = getAllComponentPageMeta()
const exports = []

for (const { slug, categoryId } of meta) {
  if (SKIP.has(slug)) continue
  const name = slugToComponentModuleName(slug)
  const dir = join(componentsDir, categoryId)
  mkdirSync(dir, { recursive: true })
  const content = stubTemplate.replace(/SLUG/g, slug)
  writeFileSync(join(dir, `${name}.jsx`), content, 'utf8')
  exports.push(`export { default as ${name} } from './${categoryId}/${name}'`)
}

const barrel = `/**
 * Auto-generated by scripts/generate-component-stubs.mjs — do not edit by hand.
 * Re-run: npm run generate:component-stubs
 */
${exports.join('\n')}
`

writeFileSync(join(componentsDir, 'allStubComponents.js'), barrel, 'utf8')
console.log(`Wrote ${exports.length} stub components into category folders + allStubComponents.js`)
